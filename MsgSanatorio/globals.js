/**
 * @param {Object} arg0 // TODO generated, please specify type and doc
 * @param {Object} arg1 // TODO generated, please specify type and doc
 * @param {Object} arg2 // TODO generated, please specify type and doc
 * @param {Object} arg3 // TODO generated, please specify type and doc
 * @param {Object} arg4 // TODO generated, please specify type and doc
 * @param {Object} arg5 // TODO generated, please specify type and doc
 * @param {Object} arg6 // TODO generated, please specify type and doc
 * @param {Object} arg7 // TODO generated, please specify type and doc
 *
 * @properties={typeid:24,uuid:"5137D3CC-20F4-4348-BD2C-88152BA5048B"}
 */
function core_showWcGenericDialog(arg0, arg1, arg2, arg3, arg4, arg5, arg6, arg7)
{
	var title = arg0;
	var msg = arg1
	var mthd = arg2 //Metodo a ejecutar despues de cerrar el Dialogo
	var icon = arg3 //puede ser'error, información, pregunta, atención, exclamación'
	var btn1 = arg4 //btn1
	var btn2 = arg5 //btn2
	var btn3 = arg6 //btn3
	var btn4 = arg7 //btn4
	
	if(!btn1) btn1 = 'OK'
	if(!btn2) btn2 = null
	if(!btn3) btn3 = null
	if(!btn4) btn4 = null
	
	//Metodo a ejecutar despues de mostrar el Dialogo
	globals.core_dlg_methodToExecute = mthd
	
	
	//NOTE: Change "-1" to "3" if you want to use the built-in dialog
	if(application.getApplicationType() <= -1)  //smart client/developer/server
	{
		//show the "real" dialog type
		switch( icon )
		{
			case 'error': plugins.dialogs.showErrorDialog( title,  msg, btn1, btn2, btn3, btn4); break;
			case 'información': plugins.dialogs.showInfoDialog( title,  msg, btn1, btn2, btn3, btn4); break;
			case 'pregunta': plugins.dialogs.showQuestionDialog( title,  msg, btn1, btn2, btn3, btn4); break;
			case 'atención': plugins.dialogs.showWarningDialog( title,  msg, btn1, btn2, btn3, btn4); break;
			default: plugins.dialogs.showInfoDialog( title,  msg, btn1, btn2, btn3, btn4);
		}
	}
	else
	{
		
		//setear botones
		forms.msg_generico.setup_buttons(btn1, btn2, btn3, btn4)
		
		if(!title || title == undefined) title = ''
		if(!msg || msg == undefined) msg = ''
		
		
		forms.msg_generico.hide_allIcons();
		globals.core_dlg_title = title
		globals.core_dlg_text = msg
		
		//show the right message box - either HTML or text based on message
		if(msg.indexOf('<html>') >= 0)
		{
			forms.msg_generico.elements.fld_dlgHTML.visible = true
			forms.msg_generico.elements.fld_dlgText.visible = false
		}
		else
		{
			forms.msg_generico.elements.fld_dlgHTML.visible = false
			forms.msg_generico.elements.fld_dlgText.visible = true
		}
		
		switch( icon )
		{
			case 'error': forms.msg_generico.show_iconError(); break;
			case 'información': forms.msg_generico.show_iconInfo(); break;
			case 'pregunta': forms.msg_generico.show_iconQuestion(); break;
			case 'atención': forms.msg_generico.show_iconWarning(); break;
			case 'error2': forms.msg_generico.show_iconForbidden(); break;
			default: forms.msg_generico.show_iconInfo();
		}
		
		var win = application.createWindow("Dialog", JSWindow.MODAL_DIALOG);
		win.setInitialBounds(-1, -1, -1, -1);
		win.title= title;
		win.resizable = false
		win.show(forms.msg_generico);
	}
}

